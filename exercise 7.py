def stepstofifty():
    for x in range(51):
        print(x)

def redtofifty():
    for x in range(51):
        x = 50-x
        print(x)

def thirty():
    for x in range(29,50):
        x = x + 1
        print(x)

def redtofifty2():
    for x in range(50,9,-2):
        print(x)

